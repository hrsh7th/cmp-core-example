local spec = require('complete.spec')
local Keymap = require('complete.kit.Vim.Keymap')
local TriggerContext = require('complete.core.TriggerContext')

describe('complete.core', function()
  describe('TriggerContext', function()
    for _, mode in ipairs({ 'i', 'c' }) do
      describe(mode, function()
        describe('.create', function()
          it('should create trigger context', function()
            spec.reset()
            Keymap.spec(function()
              Keymap.send(mode == 'c' and ':' or 'i'):await()
              Keymap.send(Keymap.termcodes('foo+bar<Left><Left><Left>')):await()
              local trigger_context = TriggerContext.create({ trigger_character = '+' })
              assert.equals(mode, trigger_context.mode)
              assert.equals(4, trigger_context.character)
              assert.equals('foo+bar', trigger_context.text)
              assert.equals('foo+', trigger_context.text_before)
              assert.equals('foo+', trigger_context:get_query(1))
              assert.equals(false, trigger_context.force)
              assert.equals('+', trigger_context.before_character)
              Keymap.send('<Esc>'):await()
            end)
          end)
        end)
      end)
    end
  end)
end)
